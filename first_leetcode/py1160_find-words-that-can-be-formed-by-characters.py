import collections


class Solution:
    def countCharacters(self, words: list[str], chars: str) -> int:
        char_table = collections.Counter(chars)
        ans = 0
        for word in words:
            char_table_copy = char_table.copy()
            is_good = True
            for ch in word:
                if ch in char_table_copy and char_table_copy[ch] > 0:
                    char_table_copy[ch] -= 1
                else:
                    is_good = False
                    break
            if is_good:
                ans += len(word)

        return ans



